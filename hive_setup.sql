CREATE TABLE IF NOT EXISTS silver_preprocessed (
  ID STRING,
  Source STRING,
  Severity INT,
  Start_Time TIMESTAMP,
  End_Time TIMESTAMP,
  Start_Lat DOUBLE,
  Start_Lng DOUBLE,
  End_Lat DOUBLE,
  End_Lng DOUBLE,
  `Distance(mi)` DOUBLE,
  Description STRING,
  Street STRING,
  City STRING,
  County STRING,
  State STRING,
  Zipcode STRING,
  Country STRING,
  Timezone STRING,
  Airport_Code STRING,
  Weather_Timestamp TIMESTAMP,
  `Temperature(F)` DOUBLE,
  `Wind_Chill(F)` DOUBLE,
  `Humidity(%)` DOUBLE,
  `Pressure(in)` DOUBLE,
  `Visibility(mi)` DOUBLE,
  Wind_Direction STRING,
  `Wind_Speed(mph)` DOUBLE,
  `Precipitation(in)` DOUBLE,
  Weather_Condition STRING,
  Amenity BOOLEAN,
  Bump BOOLEAN,
  Crossing BOOLEAN,
  Give_Way BOOLEAN,
  Junction BOOLEAN,
  No_Exit BOOLEAN,
  Railway BOOLEAN,
  Roundabout BOOLEAN,
  Station BOOLEAN,
  Stop BOOLEAN,
  Traffic_Calming BOOLEAN,
  Traffic_Signal BOOLEAN,
  Turning_Loop BOOLEAN,
  Sunrise_Sunset STRING,
  Civil_Twilight STRING,
  Nautical_Twilight STRING,
  Astronomical_Twilight STRING
)
PARTITIONED BY (event_date DATE)
STORED AS PARQUET;
